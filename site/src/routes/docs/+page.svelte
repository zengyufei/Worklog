<h1>Introduction</h1>

<p class="lead">
    Worklog is a local-first desktop project manager designed for engineers and creators who value speed, privacy, and seamless Git collaboration.
</p>

<div class="my-12 p-8 rounded-2xl bg-white/5 border border-white/10 backdrop-blur-sm">
    <h3 class="text-xl font-serif text-white mb-4">Philosophy</h3>
    <p class="mb-0">
        We believe that your project data should belong to you, not a cloud provider. 
        Worklog combines the power of a modern Kanban interface with the reliability of a local SQLite database and the distribution capabilities of Git.
    </p>
</div>

<h2>Core Features</h2>

<ul>
    <li><strong>Local-First:</strong> Your data stays on your machine. Zero latency, works 100% offline.</li>
    <li><strong>Git-Native Sync:</strong> Background synchronization with private repositories for team collaboration.</li>
    <li><strong>Command Palette:</strong> Keyboard-driven workflow to manage tasks without leaving your home row.</li>
    <li><strong>Multiple Views:</strong> Switch between Kanban, Table, and upcoming Timeline views instantly.</li>
</ul>

